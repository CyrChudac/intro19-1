Obsluha databazy telefonickej spolocnosti

Mame databazu, ktora je v ulozena v skrytom subore napriklad .telDb.
Databaza obsahuje meno a priezvisko (prijmeni) zakaznika, statistiku volani
(prevolane minuty, smsky), datum vzniku zmluvy a ukoncenie platnosti,
pokojne aj dalsie veci ako napriklad tarif, typ sluzby..

Ovladanie si predstavujem pomocou nejakych prepinacov, napr.
tel.sh -help -> vypise mozne prikazy
tel.sh -existing -> vypise existujuce zmluvy
tel.sh -show "meno prijmeni" -> vypise informacie o danej osobe
. . .
